<h2>Mailing list</h2>
<p>The mailing list is where we announce our hikes, weekend trips and socials, and anything else we need to communicate. For technical reasons we regret that we are currently unable to provide signing up to the mailing list via the website.</p>
<div class="half">
  <h3>Subscribe</h3>
  <p>To join the mailing list, send a blank email with the subject <strong>subscribe</strong> to <br />
  <a href="mailto:umhc_club-request@lists.umhc.org.uk?subject=subscribe">umhc_club-request@lists.umhc.org.uk</a>.</p>
  <p>You should receive a confirmation email; please check your spam folder if it doesn't arrive.</p>
</div>
<div class="half end">
  <h3>Unsubscribe</h3>
  <p>To leave the mailing list, send a blank email with the subject <strong>unsubscribe</strong> to <br />
  <a href="mailto:umhc_club-request@lists.umhc.org.uk?subject=unsubscribe">umhc_club-request@lists.umhc.org.uk</a>.</p>
  <p>You should receive a confirmation email; please check your spam folder if it doesn't arrive.</p>
</div>

